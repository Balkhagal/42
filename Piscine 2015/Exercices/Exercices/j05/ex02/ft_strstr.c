char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int e;

	i = 0;
	while (str[i] != '\0')
	{
		e = 0;
		while (to_find[e] == str[i + e])
		{
			if (to_find[e + i] == '\0')
			{
				return (str + i);
			}
			e++;
		}
		i++;
	}
	return (0);
}
