char *ft_strcapitalize(char *str)
{
	int i;
	int j;

	i = 0;
	while (str[i])
	{
		j = 0;
		while ((str[i] >= 'A' && str[i] <= 'Z')
				|| (str[i] >= 'a' && str[i] <= 'z')
				|| (str[i] >= '0' && str[i] <= '9'))
		{
			if (j == 0 && str[i] <= 'z' && str[i] >= 'a')
				str[i] -= 32;
			j++;
			i++;
		}
		i++;
	}
	return (str);
}
